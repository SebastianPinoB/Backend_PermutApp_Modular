create table if not exists pais (
  pais_id integer generated by default as identity primary key,
  pais_nombre varchar(120) not null
);

create table if not exists region (
  regi_id integer generated by default as identity primary key,
  regi_nombre varchar(120) not null,
  pais_id integer not null references pais(pais_id) on delete cascade
);

create table if not exists ciudad (
  ciudad_id integer generated by default as identity primary key,
  ciudad_nombre varchar(120) not null,
  regi_id integer not null references region(regi_id) on delete cascade
);

create table if not exists comuna (
  comu_id integer generated by default as identity primary key,
  comu_nombre varchar(120) not null,
  ciu_id integer not null references ciudad(ciudad_id) on delete cascade
);

create table if not exists direccion (
  add_id integer generated by default as identity primary key,
  add_calle varchar(180),
  add_numero integer,
  add_letra varchar(20),
  comu_id integer references comuna(comu_id)
);

create table if not exists punto_encuentro (
  sug_id_punto_encuentro integer generated by default as identity primary key,
  latitud double precision,
  longitud double precision,
  referencias text,
  comu_id integer references comuna(comu_id)
);

create table if not exists estacion_metro (
  id integer generated by default as identity primary key,
  nombre varchar(120) not null,
  linea varchar(20) not null,
  orden integer,
  es_combinacion boolean default false,
  latitud double precision,
  longitud double precision,
  direccion varchar(220),
  comuna varchar(120)
);

create unique index if not exists pais_nombre_uidx on pais (lower(pais_nombre));
create unique index if not exists region_nombre_pais_uidx on region (lower(regi_nombre), pais_id);
create unique index if not exists ciudad_nombre_region_uidx on ciudad (lower(ciudad_nombre), regi_id);
create unique index if not exists comuna_nombre_ciudad_uidx on comuna (lower(comu_nombre), ciu_id);
create unique index if not exists estacion_metro_nombre_linea_uidx on estacion_metro (lower(nombre), lower(linea));

alter table estacion_metro
  add column if not exists latitud double precision,
  add column if not exists longitud double precision,
  add column if not exists direccion varchar(220),
  add column if not exists comuna varchar(120);

