update estacion_metro em
set
  orden = seed.orden,
  es_combinacion = seed.es_combinacion,
  latitud = seed.latitud,
  longitud = seed.longitud,
  direccion = coalesce(seed.direccion, em.direccion),
  comuna = coalesce(seed.comuna, em.comuna)
from tmp_estacion_metro_seed seed
where lower(em.nombre) = lower(seed.nombre)
  and lower(em.linea) = lower(seed.linea);

insert into estacion_metro (nombre, linea, orden, es_combinacion, latitud, longitud, direccion, comuna)
select seed.nombre, seed.linea, seed.orden, seed.es_combinacion, seed.latitud, seed.longitud, seed.direccion, seed.comuna
from tmp_estacion_metro_seed seed
where not exists (
  select 1
  from estacion_metro em
  where lower(em.nombre) = lower(seed.nombre)
    and lower(em.linea) = lower(seed.linea)
);
